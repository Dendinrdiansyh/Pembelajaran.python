a = int(input("inputkan nilai a: "))
b = int(input("inputkan nilai b: "))

# apakah a sama dengan b
c = a == b
print("apakah", a, " == ", b, " : ", c)

# apakah a > b
c = a > b
print("apakah", a, " > ", b, " : ", c)

# apakah a < b
c = a < b
print("apakah", a, " < ", b, " : ", c)

# apakah a <= b
c = a <= b
print("apakah", a, " <= ", b, " : ", c)

# apakah a >= b
c = a >= b
print("apakah", a, " >= ", b, " : ", c)

# apakah a != b
c = a != b
print("apakah", a, " != ", b, " : ", c)